/*
===========================================
  ポインタ課題 - ステップ2（値の変更）
===========================================

【課題2】ポインタを使った値の変更
以下の要件を満たすプログラムを作成してください。

要件:
1. int型の変数 score を宣言し、50 を代入する
2. int型のポインタ変数 p を宣言し、score のアドレスを代入する
3. score の値を出力する
4. ポインタ p を使って score の値を 100 に変更する
5. 再度 score の値を出力する

期待される出力:
変更前の score: 50
変更後の score: 100

ヒント:
*p = 100;  // ポインタが指す場所の値を変更

===========================================
*/

#include <stdio.h>

int main() {
    // ここにコードを書いてください
    int score = 50, *p;
    p = &score;
    printf("変更前の score: %d\n",score);
    *p = 100;
    printf("変更後の score: %d\n",score);

    return 0;
}
