/*
===========================================
  ポインタ課題 - ステップ1（基礎）
===========================================

【課題1】ポインタの宣言と基本操作
以下の要件を満たすプログラムを作成してください。

要件:
1. int型の変数 age を宣言し、25 を代入する
2. int型のポインタ変数 ptr を宣言する
3. ptr に age のアドレスを代入する
4. 以下を出力する:
   - age の値
   - age のアドレス
   - ptr の値（アドレス）
   - ptr が指す値

期待される出力例:
age の値: 25
age のアドレス: 0x7ffeeb3c8a4c
ptr の値: 0x7ffeeb3c8a4c
ptr が指す値: 25

※アドレスの値は実行環境によって異なります

===========================================
*/

#include <stdio.h>

int main() {
    // ここにコードを書いてください
    int age = 25, *ptr;
    ptr = &age;

    printf("age の値\t: %d\n",age);
    printf("age のアドレス\t: %p\n",&age);
    printf("ptr の値\t: %p\n",ptr);
    printf("ptr が指す値\t: %d\n",*ptr);
    
    return 0;
}
