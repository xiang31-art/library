/*
===========================================
  ポインタ課題 - ステップ4（配列とポインタ）
===========================================

【課題4】配列をポインタで操作する
以下の要件を満たすプログラムを作成してください。

要件:
1. int型の配列 numbers を宣言し、{10, 20, 30, 40, 50} で初期化する
2. int型のポインタ変数 ptr を宣言し、配列の先頭アドレスを代入する
3. ポインタを使って配列の全要素を出力する（for文を使用）
4. ポインタを使って配列の全要素を2倍にする
5. 再度、配列の全要素を出力する

期待される出力:
元の配列: 10 20 30 40 50
2倍後の配列: 20 40 60 80 100

ヒント:
- 配列名はポインタとして使える: ptr = numbers;
- ポインタ演算: *(ptr + i) で i番目の要素にアクセス
- または: ptr[i] でもアクセス可能

===========================================
*/

#include <stdio.h>

int main() {
    // ここにコードを書いてください
    int numbers[5] = {10, 20, 30, 40, 50};
    int *ptr;
    ptr = numbers;
    
    printf("元の配列   : ");
    for(int i = 0; i < 5; i++){
      printf(" %d",numbers[i]);
    }
    printf("\n");
    
    for(int i = 0; i < 5; i++){
      *(ptr + i) *= 2;
    }

    printf("2倍後の配列: ");
    for(int i  = 0; i < 5; i++){
      printf(" %d",numbers[i]);
    }
    printf("\n");

    return 0;
}
